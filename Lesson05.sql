-- Lesson 05: Reporting Aggregated Data Using the Group Functions (Part 1)

SELECT MIN(SALARY),MAX(SALARY),AVG(SALARY),SUM(SALARY)
FROM EMPLOYEES
WHERE JOB_ID LIKE '%REP%';

SELECT LAST_NAME
FROM EMPLOYEES
WHERE HIRE_DATE='17-JUN-87';

SELECT COUNT(*)
FROM EMPLOYEES;

SELECT COUNT(COMMISSION_PCT)
FROM EMPLOYEES;

SELECT COUNT(NVL(COMMISSION_PCT,0))
FROM EMPLOYEES;

SELECT COUNT(COMMISSION_PCT)
FROM EMPLOYEES;

SELECT COUNT(COMMISSION_PCT)
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL;

SELECT COUNT(DISTINCT MANAGER_ID)
FROM EMPLOYEES;














