SELECT EMPLOYEE_ID,SALARY,LPAD(SALARY,5,'$'),RPAD(SAlARY,6,'$')
FROM EMPLOYEES;

SELECT LAST_NAME,TRIM('K' FROM LAST_NAME)
FROM EMPLOYEES;

SELECT EMPLOYEE_ID,LAST_NAME,JOB_ID,REPLACE(JOB_ID,'_','-')
FROM EMPLOYEES
WHERE REPLACE(JOB_ID,'_','-') LIKE 'AD-%';

SELECT EMPLOYEE_ID,LAST_NAME,JOB_ID,REPLACE(JOB_ID,'_','-') 
FROM EMPLOYEES
WHERE REPLACE(JOB_ID,'_','-') LIKE 'AD-%';

SELECT EMPLOYEE_ID, LAST_NAME,SALARY/3,ROUND(SALARY/3,2),TRUNC(SALARY/3,2)
FROM EMPLOYEES;

SELECT EMPLOYEE_ID,LAST_NAME,MOD(SALARY,3)
FROM EMPLOYEES;

