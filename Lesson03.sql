-- Lesson 03: Using Single-Row Functions (Part 1)

SELECT LAST_NAME,UPPER(LAST_NAME) AS UNAME,LOWER(LAST_NAME) AS LNAME,
       INITCAP(LAST_NAME) AS INAME
FROM EMPLOYEES
WHERE LOWER(LAST_NAME) = 'King';

SELECT FIRST_NAME,LAST_NAME,FIRST_NAME || ' ' || LAST_NAME AS "Full Name",
       CONCAT(FIRST_NAME,LAST_NAME)
FROM EMPLOYEES;

SELECT LAST_NAME,SUBSTR(LAST_NAME,2,2)
FROM EMPLOYEES;


SELECT LAST_NAME,LENGTH(LAST_NAME)
FROM EMPLOYEES
WHERE LENGTH(LAST_NAME) >= 5;

SELECT LAST_NAME,INSTR(LAST_NAME, ' ')
FROM EMPLOYEES
WHERE INSTR(LAST_NAME, ' ') <>0;

